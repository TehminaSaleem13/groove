      @result = @result.merge({ 'products' => make_products_list(@products['products']),
                                'products_count' => get_products_count
                              })
      @result['products_count']['search'] = @products['count']
    else
      @result['status'] = false
      @result['message'] = 'Improper search string'
    end

    render json: @result
  end

